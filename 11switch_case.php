<?php
// learning switch case video 11

$age = 12;

switch($age){
    case 12:
        echo "your age is 12 <br>";
        break;
 
    case 34:
        echo "your age is 34 <br>";
        break;

    case 24:
        echo "your age is 24 <br>";
        break;

    case 56:
        echo "your age is 56 <br>";
        break;
        
    default:
        echo "your age is different <br>";
        break;
}




?>